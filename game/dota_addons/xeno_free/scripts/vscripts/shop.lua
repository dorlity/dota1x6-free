function shop:CopyTable(orig) -- Откопипастить таблицу
    local orig_type = type(orig)
    local copy
    if orig_type == 'table' then
        copy = {}
        for orig_key, orig_value in next, orig, nil do
            copy[shop:CopyTable(orig_key)] = shop:CopyTable(orig_value)
        end
        setmetatable(copy, shop:CopyTable(getmetatable(orig)))
    else -- number, string, boolean, etc
        copy = orig
    end
    return copy
end

function shop.remove_item(tbl,item) -- Удалить итем
    local i,max=1,#tbl
    while i<=max do
        if tbl[i] == item then
            table.remove(tbl,i)
            i = i-1
            max = max-1
        end
        i= i+1
    end
    return tbl
end

